Divition = X/Y
Modulus = X%Y
Exponentiation = X**Y
Floor_Division = X//Y
print(Addition)
print(Subtraction)
print(Multiplication)
print(Divition)
print(Modulus)
print(Exponentiation)
print(Floor_Division)

#Comparison Operators
a = 7
b = 13
Equal_to = a==b
Not_equal_to = a!=b
Less_than = a<b
Greater_than =a>b
LessThan_or_EqualTO = a<=b
GreaterThan_or_EqualsTo = a>=b
print(Equal_to)
print(Not_equal_to)
print(Less_than)
