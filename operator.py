print(GreaterThan_or_EqualsTo)

#Logical Operators
x = True
y = False

Logical_AND = x and y 
Logical_OR = x or y 
Logical_NOT = not x
print(Logical_AND)
print(Logical_OR)
print(Logical_NOT) 

#Assignment Operators
x = 5
x += 2 # Equivalent to x = x + 2
print(x)

#Bitwise Operators
x = 2
y = 5

Bitwise_AND = x & y
Bitwise_OR = x | y
Bitwise_XOR = x ^ y
Left_Shift = x << y
Right_Shift = x >> y
print(Bitwise_AND)
print(Bitwise_OR)
print(Bitwise_XOR)
print(Left_Shift)
print(Right_Shift)

#Membership Operators
fruits = ["apple", "banana", "cherry"]
is_apple_in_list = "apple" in fruits
is_mango_not_in_list = "mango" not in fruits
print(is_apple_in_list)
print(is_mango_not_in_list)

#Identity Operators
x = [1, 2, 3]
y = x
is_same_object = x is y
print(is_same_object)

